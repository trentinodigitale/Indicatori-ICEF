package c_elab.pat.barch21;

public class Perc_condominiale extends QDati {
	/** ritorna il valore double da assegnare all'input node
	 * @return double
	 */
	public double getValue() {
		
		try {
			double perc_condominiale = records.getDouble(1, records.getIndexOfColumnName("perc_condominiale"));
			
			if (perc_condominiale == 0 )  {
				perc_condominiale = 1;
			}else {
				perc_condominiale = perc_condominiale /100;
			}
			return perc_condominiale;
			
		} catch (Exception e) {
			e.printStackTrace();
			return 0;
		}
	}
}
